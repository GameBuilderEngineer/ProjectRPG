//===================================================================================================================================
//【imgui管理クラス】
//===================================================================================================================================
class ImguiManager
{
private:
	bool show_demo_window = false;
	bool show_another_window = false;
public:
	ImVec4 clearColor = ImVec4(0.00f, 0.00f, 0.00f, 1.00f);
	ImguiManager(HWND wnd);
	~ImguiManager();
	void beginFrame();
	void update();

	void render();		//with[EndFrame()]
	void reset();
	//===================================================================================================================================
	//【ImGuiの開始】
	// const char* name…ImGuiの名前
	// ※必要な処理が済んだら、他のGUIを起動する前に必ずendImGuiを呼び出すこと
	//===================================================================================================================================
	void beginImGui(const char* name);

	//===================================================================================================================================
	//【ImGuiの開始】
	// const char* name…ImGuiの名前
	// bool* open…開閉フラグ。毎度初期化を行うと開閉処理にならないので注意。
	// ※必要な処理が済んだら、他のGUIを起動する前に必ずendImGuiを呼び出すこと
	//===================================================================================================================================
	void beginImGui(const char* name,bool* open);

	//===================================================================================================================================
	//【ImGuiの開始】
	//===================================================================================================================================
	void endImGui();

	//getter
	DWORD getClearColor();
};

